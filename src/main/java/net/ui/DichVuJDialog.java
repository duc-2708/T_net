    private void tblLoaiDVMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblLoaiDVMouseClicked
        // TODO add your handling code here:
        int hangDuocChon = tblLoaiDV.getSelectedRow();
        String IDLoaiDV = tblLoaiDV.getValueAt(hangDuocChon, 0).toString();
        List<DichVu> dichVuLst = DichVuDAO.readDV(IDLoaiDV);

        DefaultTableModel tblDichVu = (DefaultTableModel) this.tblDV.getModel();
        tblDichVu.setRowCount(0);

        for (DichVu dichVu : dichVuLst) {
            tblDichVu.addRow(new Object[]{
                dichVu.getIDDV(),
                dichVu.getTenDV(),
                dichVu.getGia(),});
        }
    }//GEN-LAST:event_tblLoaiDVMouseClicked

    private void btnOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOrderActionPerformed
        // TODO add your handling code here:
        String IDMay = txtID.getText();
        
        int SoLuong = (int) spnSoLuong.getValue();
        if(SoLuong < 1){
            JOptionPane.showMessageDialog(this, "so luong lơn hon 0");
        }
        int hangDuocChonLDV = tblLoaiDV.getSelectedRow();
        String IDLoaiDV = tblLoaiDV.getValueAt(hangDuocChonLDV, 0).toString();
        
        int hangDuocChonDV = tblDV.getSelectedRow();
        
        String IDDV = tblDV.getValueAt(hangDuocChonDV, 0).toString();
        String TenDV = tblDV.getValueAt(hangDuocChonDV, 1).toString();
        Double Gia = Double.parseDouble(tblDV.getValueAt(hangDuocChonDV, 2).toString());
        DichVuMay_Tam dvTam = new DichVuMay_Tam(IDMay, IDDV, TenDV, IDLoaiDV, SoLuong, Gia);
        
        int ketQua = DichVuDAO.orderDV(dvTam);
        if(ketQua >= 1){
            Double thanhTien = SoLuong * Gia;
            JOptionPane.showMessageDialog(this, "Order thành công \n thành tiên:"+thanhTien);
        }else{
            JOptionPane.showMessageDialog(this, "order thất bại");
        }
    }//GEN-LAST:event_btnOrderActionPerformed

