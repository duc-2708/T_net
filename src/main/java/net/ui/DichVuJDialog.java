    public void readLoaiDichVu() {
        List<LoaiDichVu> loaiDichVuLst = DichVuDAO.readLDV();
        DefaultTableModel tblLoaiDV = (DefaultTableModel) this.tblLoaiDV.getModel();
        tblLoaiDV.setRowCount(0);
        for (LoaiDichVu dichVu : loaiDichVuLst) {
            tblLoaiDV.addRow(new Object[]{
                dichVu.getIDLoaiDv(),
                dichVu.getTenLoaiDV()
            });
        }
    }

